// Hjälpfunktion för att filtrera kommuner baserat på sökning
export const filterMunicipalities = (searchTerm: string) => {
  if (!searchTerm) return [];
  
  const normalizedSearch = searchTerm.toLowerCase().trim();
  
  return swedishMunicipalities
    .filter(municipality => 
      municipality.name.toLowerCase().startsWith(normalizedSearch) ||
      municipality.name.toLowerCase().includes(normalizedSearch)
    )
    .sort((a, b) => {
      // Prioritera exakta matchningar
      const aStarts = a.name.toLowerCase().startsWith(normalizedSearch);
      const bStarts = b.name.toLowerCase().startsWith(normalizedSearch);
      
      if (aStarts && !bStarts) return -1;
      if (!aStarts && bStarts) return 1;
      
      // Annars alfabetisk ordning
      return a.name.localeCompare(b.name, 'sv');
    })
    .slice(0, 10); // Visa max 10 förslag
};
